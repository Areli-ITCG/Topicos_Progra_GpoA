    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        nombre = jTextField1.getText();
        password = new String(jPasswordField1.getPassword()); 
        con= cn.conectar();
        try {
            //Sentencia con par치metros
             String sql="select tipo from usuarios where usuario = ? and password = ?";
             PreparedStatement sentencia= con.prepareStatement(sql);
             sentencia.setString(1,nombre);
             sentencia.setString(2, password);
             ResultSet rs = sentencia.executeQuery();//Guarda el resultado de la consulta
             while(rs.next())
                 tipo = rs.getInt("tipo");//Nombre de la columna
             System.out.println(tipo);
            } catch (Exception e) {
            System.out.println(e.getMessage());
        }
       if(tipo == 0)
            {
                JOptionPane.showMessageDialog(null, "Usuario o Password equivocado");
                jTextField1.setText(null);
                jPasswordField1.setText(null);
                jTextField1.requestFocus();
            }
            else
                if(tipo == 1)
                {
                 JOptionPane.showMessageDialog(null, "Bienvenido Administrador");
                 super.dispose();
                 PrincipalMenu pm = new PrincipalMenu();
                 pm.setVisible(true);
                }
                else 
                    if(tipo == 2)
                    {
                        JOptionPane.showMessageDialog(null, "Bienvenida Secretaria");
                        super.dispose();
                        PrincipalMenu pm = new PrincipalMenu(tipo);
                        pm.setVisible(true);
                    }
                    else if(tipo == 3)
                    {
                        JOptionPane.showMessageDialog(null, "Bienvenido/a Cajero/a");
                        super.dispose();
                        PrincipalMenu pm = new PrincipalMenu(tipo);
                        pm.setVisible(true);
                    }
   
    }//GEN-LAST:event_jButton1ActionPerformed

